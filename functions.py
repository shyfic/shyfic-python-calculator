import math

def subtraction(x, y):
    result = x - y
    return result

def addition(x, y):
    result = x + y
    return result

def multiplication(x, y):
    result = x * y
    return result

def division(x, y):
    result = x / y
    return result

def exponent(x, y):
    result = math.pow(x, y)
    return result

def root(x, y):
    result = math.pow(x, 1/y)
    return result